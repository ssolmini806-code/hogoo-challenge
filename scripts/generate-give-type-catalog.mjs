// React 보상 화면의 유형 원본을 classic script 검사 화면에서도 쓰도록 변환한다.
// 이 파일은 public 산출물을 기계적으로 만들 뿐, 카피 원본을 갖지 않는다.
import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { TYPES, AXES } from '../src/rewards/give-type-data.js';

const target = fileURLToPath(new URL('../public/give-type-catalog.generated.js', import.meta.url));
const payload = { types: TYPES, axes: AXES };
const source = `// GENERATED by scripts/generate-give-type-catalog.mjs — 직접 수정하지 마세요.\nwindow.GIVE_TYPE_CATALOG = Object.freeze(${JSON.stringify(payload, null, 2)});\n`;

await writeFile(target, source, 'utf8');
